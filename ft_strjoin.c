#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char		*str;
	unsigned	i;

	i = 0;
	str = ft_strnew((size_t)(ft_strlen((char *)s1) + ft_strlen((char *)s2)));
	if (!str)
		return (NULL);
	while (*s1)
		str[i++] = *s1++;
	while (*s2)
		str[i++] = *s2++;
	return (str);
}

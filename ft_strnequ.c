#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	unsigned	i;

	i = n;
	while (*s1++ == *s2++ && i-- > 0)
		if (*s1 == *s2 && *s1 == '\0')
			return (1);
	return (0);
}
